    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        if(untxt.getText().length()==0)  // Checking for empty field
        JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up all fields");
     else if(pntxt.getPassword().length==0)  // Checking for empty field
        JOptionPane.showMessageDialog(null, "Empty fields detected ! Please fill up all fields");
     else{
         String user = untxt.getText();   // Collecting the input
         char[] pass = pntxt.getPassword(); // Collecting the input
         String pwd = String.copyValueOf(pass);  // converting from array to string
         if(validate_login(user,pwd))
         {
             this.dispose();
             if(role == 1){
                 new adminframe().setVisible(true);
                 
            // new Tables().setVisible(true);   
             }
             else {
                new Tables().setVisible(true);
             }
         }
         else{
            JOptionPane.showMessageDialog(null, "Incorrect Login Credentials");
         }
     }        
    }//GEN-LAST:event_loginbtnActionPerformed

