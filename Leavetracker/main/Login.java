private boolean validate_login(String username,String password) {
    boolean rt = false;
   try{           
      // Class.forName("com.mysql.jdbc.Driver");  // MySQL database connection
      // Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/cd_hrsample?" + "user=root&password=");
       db = new Database();
      // String query = "Select * from account_table where username=? and password=?";
       PreparedStatement pst = db.conn.prepareStatement("Select * from account_table where username=? and password=?");
       pst.setString(1, username); 
       pst.setString(2, password);
       ResultSet rs = pst.executeQuery();                        
       if(rs.next()) {
           role = rs.getInt("role");
          // return true;    
       } 
       if (role!=null) {
                rt = true;
        }
       db.closeConn();
      // rs.close();
   }
   catch(Exception e){
       e.printStackTrace();
       
   }    
   return rt;
}
    
